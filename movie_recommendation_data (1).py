dataframe = movies[['movie_id','title','tags']]

dataframe['tags']= dataframe['tags'].apply(lambda x:" ".join(x))

dataframe.head()

import nltk

from nltk.stem.porter import PorterStemmer
ps = PorterStemmer()

def stem(text):
  y= []
  for i in text.split():
    y.append(ps.stem(i))

  return " ".join(y)

dataframe['tags'].apply(stem)

dataframe['tags'][0]

dataframe['tags']= dataframe['tags'].apply(lambda x:x.lower())

dataframe.head()

from sklearn.feature_extraction.text import CountVectorizer
cv = CountVectorizer(max_features=5000,stop_words='english')

vectors = cv.fit_transform(dataframe['tags']).toarray()

vectors[0]

